// Get all fossils for a specific dinosaur set
export const getFossilsBySet = (setId: string): Fossil[] =>
  FOSSIL_DATA.filter(fossil => fossil.setId === setId);

// Get all unique dinosaur sets
export const getDinosaurSets = (): string[] =>
  [...new Set(FOSSIL_DATA.map(f => f.setId))];

// Check if a set is complete
export const isSetComplete = (setId: string, foundFossils: string[]): boolean => {
  const setFossils = getFossilsBySet(setId);
  return setFossils.every(fossil => foundFossils.includes(fossil.id));
};

// Get completion percentage for a set
export const getSetCompletion = (setId: string, foundFossils: string[]): number => {
  const setFossils = getFossilsBySet(setId);
  const found = setFossils.filter(fossil => foundFossils.includes(fossil.id));
  return Math.round((found.length / setFossils.length) * 100);
};
