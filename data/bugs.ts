export const getBugsByLocation = (location: string): Bug[] =>
  BUG_DATA.filter(bug => bug.location === location);

export const getBugsByRarity = (rarity: string): Bug[] =>
  BUG_DATA.filter(bug => bug.rarity === rarity);

export const getActiveBugs = (hour: number, month: number, weather: string): Bug[] =>
  BUG_DATA.filter(bug => {
    const [start, end] = bug.activeHours;
    const hourActive = start < end
      ? hour >= start && hour < end
      : hour >= start || hour < end;
    const monthActive = bug.activeMonths.includes(month);
    const weatherActive = bug.weather === 'any' || bug.weather === weather;
    return hourActive && monthActive && weatherActive;
  });
