export const getFishByLocation = (location: string): Fish[] =>
  FISH_DATA.filter(fish => fish.location === location);

export const getFishByRarity = (rarity: string): Fish[] =>
  FISH_DATA.filter(fish => fish.rarity === rarity);

export const getActiveFish = (hour: number, month: number): Fish[] =>
  FISH_DATA.filter(fish => {
    const [start, end] = fish.activeHours;
    const hourActive = start < end
      ? hour >= start && hour < end
      : hour >= start || hour < end;
    const monthActive = fish.activeMonths.includes(month);
    return hourActive && monthActive;
  });
